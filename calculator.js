let num1 = 11;
let num2 = 5; 
let operator = "+";

if (operator === "+") {
    console.log(num1 + num2);
} 
else if (operator === "-") {
    console.log(num1 - num2);
}
else if (operator === "*") {
    console.log(num1 * num2);
}
else if (operator === "/") {
    console.log(num1 / num2);
}
else if (operator === "%") {
    console.log(num1 % num2);
}
else if (operator === "**") {
    console.log(num1 ** num2);
}
else {
    console.log("Wrong Operator!");
}

